# list比较，dict有以下几个特点：

# 1.查找和插入的速度极快，不会随着key的增加而变慢；
# 2.需要占用大量的内存，内存浪费多。

# 而list相反：

# 1.查找和插入的时间随着元素的增加而增加；
# 2.占用空间小，浪费内存很少。

